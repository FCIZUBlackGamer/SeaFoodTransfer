package com.ccis.seafoodtrans.model;

public class Product {
    String id;
    String name;
    String barcode;
    String image;
    String price;
    String weight;
    String amount;
    String fishing_date;
    String user_name;
    String fisherman_id;
    String status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getFishing_date() {
        return fishing_date;
    }

    public void setFishing_date(String fishing_date) {
        this.fishing_date = fishing_date;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getFisherman_id() {
        return fisherman_id;
    }

    public void setFisherman_id(String fisherman_id) {
        this.fisherman_id = fisherman_id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
